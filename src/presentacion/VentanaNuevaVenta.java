    private void btnEliminarItemsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarItemsActionPerformed
        int[] filasSeleccionadas=tablaPresupuesto.getSelectedRows();
        if(filasSeleccionadas.length==0)
            javax.swing.JOptionPane.showMessageDialog(null, "Seleccione items a eliminar");
        else{
        int cantFilasAEliminar=filasSeleccionadas.length;
        for(int i=0;i<cantFilasAEliminar;i++){
            int fila=filasSeleccionadas[i];
            int codItem=(int)modeloPresupuesto.getValueAt(fila,0);
            presupuesto.removeItem(codItem);
        }
        actualizarListadoItems();
        actualizarMontoTotal();
        }
    }//GEN-LAST:event_btnEliminarItemsActionPerformed

   

